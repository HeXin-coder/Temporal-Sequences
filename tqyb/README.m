- this folder is used to reserve the code for lecture "7C150004 Space weather forecast"

- `tqyb_onestep.ipynb` and `tqyb_multistep.ipynb` are based on Markov Models to realize the weather forecast.
- `Markov.m` is the compiled version of `tqyb_onestep.ipynb` using matlab.
- `model.mat` is the model which is trained in `Markov.m`.
